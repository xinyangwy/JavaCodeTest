package com.xinyang.demo1;

public class ChangeChar {
    public static void main(String[] args) {
        System.out.println("北\t上\t广");
        System.out.println("-----------");
        System.out.println("北\n上\n广");
        System.out.println("-----------");
        System.out.println("北\\上\\广");
        System.out.println("-----------");
        System.out.println("北\"上\'广");
        System.out.println("-----------");
        System.out.println("北\r上\r广");//替换
        System.out.println("-----------");


    }
}
